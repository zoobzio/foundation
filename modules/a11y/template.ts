import { roles, type ARIARoleDefinitionKey } from "aria-query";

const INDENT = "  ";

const strip = (prop: string) => prop.replace(/^aria-/, "");

const quoted = (name: string) => JSON.stringify(name);

/**
 * The ARIA global states and properties: the base `roletype` role's props,
 * which every role inherits. aria-query folds these into each role's `props`,
 * so they double as the set to subtract for a role's own delta.
 */
function globalAttributes(): string[] {
  const roletype = roles.get("roletype");
  return Object.keys(roletype?.props ?? {})
    .map(strip)
    .sort();
}

/**
 * Render the `GlobalAria` union, `RoleAria` registry, and `ProhibitedAria` map
 * from the WAI-ARIA spec data in `aria-query`.
 *
 * `GlobalAria` is the base props valid on every role. Each concrete (non-
 * abstract) role maps in `RoleAria` to its supported props *minus* those globals
 * â€” the delta it adds â€” or `never` when it adds nothing. `ProhibitedAria` maps
 * each role to the props the spec forbids it (e.g. `generic` prohibits naming);
 * `AriaProps` excludes these from the global+role surface, so a role cannot
 * carry an attribute the standard bans even when it is a global. Names are
 * `aria-`-stripped to the bare authoring form the `AriaProps` mapped type
 * re-prefixes; names absent from Vue's `AriaAttributes` are dropped downstream
 * by `& keyof AllAria`.
 */
export function generateA11yTypes(): string {
  const globals = globalAttributes();
  const globalSet = new Set(globals);

  const concreteRoles = [...roles.entries()]
    .filter(([, def]) => !def.abstract)
    .map(([name]) => name)
    .sort();

  const roleEntry = (name: ARIARoleDefinitionKey) => {
    const specific = Object.keys(roles.get(name)?.props ?? {})
      .map(strip)
      .filter((prop) => !globalSet.has(prop))
      .sort();
    const value = specific.length ? specific.map(quoted).join(" | ") : "never";
    return `${INDENT}${quoted(name)}: ${value};`;
  };

  const prohibitedEntry = (name: ARIARoleDefinitionKey) => {
    // `@types/aria-query` types `prohibitedProps` as a map, but the runtime
    // value is an array of prop names; normalize both shapes to name keys.
    const raw = roles.get(name)?.prohibitedProps;
    const names = Array.isArray(raw) ? [...raw] : Object.keys(raw ?? {});
    const prohibited = names.map(strip).sort();
    const value = prohibited.length
      ? prohibited.map(quoted).join(" | ")
      : "never";
    return `${INDENT}${quoted(name)}: ${value};`;
  };

  const globalUnion = globals
    .map((name) => `${INDENT}| ${quoted(name)}`)
    .join("\n");

  return [
    "// Generated by the `a11y` module â€” do not edit.",
    "",
    `export type GlobalAria =\n${globalUnion};`,
    "",
    "export interface RoleAria {",
    ...concreteRoles.map(roleEntry),
    "}",
    "",
    "export interface ProhibitedAria {",
    ...concreteRoles.map(prohibitedEntry),
    "}",
    "",
  ].join("\n");
}